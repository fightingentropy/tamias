import {
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { dirname, extname, join, basename } from "node:path";

type DocMetadata = {
  title: string;
  description: string;
  section?: string;
  order?: number;
};

type BlogMetadata = {
  title: string;
  publishedAt: string;
  summary: string;
  image?: string;
  tag: string;
};

type DocEntry = {
  slug: string;
  metadata: DocMetadata;
  content: string;
};

type DocMetadataEntry = Omit<DocEntry, "content">;

type BlogPost = {
  slug: string;
  metadata: BlogMetadata;
  content: string;
};

type BlogMetadataEntry = Omit<BlogPost, "content">;

function parseFrontmatter<T extends Record<string, unknown>>(fileContent: string) {
  const frontmatterRegex = /---\s*([\s\S]*?)\s*---/;
  const match = frontmatterRegex.exec(fileContent);
  const frontMatterBlock = match?.[1];
  const content = fileContent.replace(frontmatterRegex, "").trim();
  const frontMatterLines = frontMatterBlock?.trim().split("\n") || [];
  const metadata: Record<string, unknown> = {};

  for (const line of frontMatterLines) {
    const [key, ...valueArr] = line.split(": ");
    if (!key) continue;

    const trimmedKey = key.trim();
    let value = valueArr.join(": ").trim();
    value = value.replace(/^['"](.*)['"]$/, "$1");
    metadata[trimmedKey] =
      trimmedKey === "order" ? Number.parseInt(value, 10) : value;
  }

  return { metadata: metadata as T, content };
}

function readMdxEntries<T extends Record<string, unknown>>(dir: string) {
  return readdirSync(dir)
    .filter((file) => extname(file) === ".mdx")
    .sort((a, b) => a.localeCompare(b))
    .map((file) => {
      const rawContent = readFileSync(join(dir, file), "utf-8");
      const { metadata, content } = parseFrontmatter<T>(rawContent);

      return {
        slug: basename(file, extname(file)),
        metadata,
        content,
      };
    });
}

const rootDir = join(import.meta.dir, "..");
const docsDir = join(rootDir, "src", "site", "content", "docs");
const postsDir = join(rootDir, "src", "site", "content", "updates");
const generatedDir = join(rootDir, "src", "site", "generated");
const docMetadataOutputPath = join(generatedDir, "doc-metadata.ts");
const blogMetadataOutputPath = join(generatedDir, "blog-metadata.ts");
const docContentOutputPath = join(generatedDir, "doc-content.ts");
const blogContentOutputPath = join(generatedDir, "blog-content.ts");
const legacyMetadataOutputPath = join(generatedDir, "content-metadata.ts");
const legacyCombinedContentOutputPath = join(generatedDir, "content-content.ts");
const legacyOutputPath = join(generatedDir, "content-manifest.ts");

const docs = readMdxEntries<DocMetadata>(docsDir) satisfies DocEntry[];
const blogPosts = readMdxEntries<BlogMetadata>(postsDir) satisfies BlogPost[];
const docEntries = docs.map(({ slug, metadata }) => ({ slug, metadata })) satisfies DocMetadataEntry[];
const blogEntries = blogPosts.map(({ slug, metadata }) => ({ slug, metadata })) satisfies BlogMetadataEntry[];
const docContentBySlug = Object.fromEntries(
  docs.map(({ slug, content }) => [slug, content]),
) satisfies Record<string, string>;
const blogContentBySlug = Object.fromEntries(
  blogPosts.map(({ slug, content }) => [slug, content]),
) satisfies Record<string, string>;

mkdirSync(dirname(docMetadataOutputPath), { recursive: true });

writeFileSync(
  docMetadataOutputPath,
  `// Generated by scripts/generate-site-content-manifest.ts. Do not edit directly.
export type DocMetadata = {
  title: string;
  description: string;
  section?: string;
  order?: number;
};
export type DocMetadataEntry = {
  slug: string;
  metadata: DocMetadata;
};

export const docEntries: DocMetadataEntry[] = ${JSON.stringify(docEntries, null, 2)};
`,
);

writeFileSync(
  blogMetadataOutputPath,
  `// Generated by scripts/generate-site-content-manifest.ts. Do not edit directly.
export type BlogMetadata = {
  title: string;
  publishedAt: string;
  summary: string;
  image?: string;
  tag: string;
};
export type BlogMetadataEntry = {
  slug: string;
  metadata: BlogMetadata;
};

export const blogEntries: BlogMetadataEntry[] = ${JSON.stringify(blogEntries, null, 2)};
`,
);

writeFileSync(
  docContentOutputPath,
  `// Generated by scripts/generate-site-content-manifest.ts. Do not edit directly.
export const docContentBySlug: Record<string, string> = ${JSON.stringify(
    docContentBySlug,
    null,
    2,
  )};
`,
);

writeFileSync(
  blogContentOutputPath,
  `// Generated by scripts/generate-site-content-manifest.ts. Do not edit directly.
export const blogContentBySlug: Record<string, string> = ${JSON.stringify(
    blogContentBySlug,
    null,
    2,
  )};
`,
);

rmSync(legacyMetadataOutputPath, { force: true });
rmSync(legacyCombinedContentOutputPath, { force: true });
rmSync(legacyOutputPath, { force: true });

console.log(
  `Generated site content metadata and content with ${docs.length} docs and ${blogPosts.length} posts.`,
);
